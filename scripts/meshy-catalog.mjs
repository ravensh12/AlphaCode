/** Per-clip pseudo entries (see block above). Hoisted function so the array
 *  literal can spread it; pure data, mirrors the parent character entry. */
function wave2Clips(charId, charName, clips) {
  return clips.map(([clip, hint]) => ({
    id: `${charId}-${clip}`,
    name: `${charName} — ${clip} clip`,
    category: 'character',
    prompt: `(animation clip from the ${charId} rig — no text-to-3d task; see scripts/meshy-wave2-clips.mjs)`,
    artStyle: 'realistic',
    tier: 6,
    sizeClass: 'character',
    placementHint: hint,
  }))
}

